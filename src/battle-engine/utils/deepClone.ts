/**
 * Deep clone an object/array to ensure immutability
 * Uses JSON serialization for simplicity (works for plain objects/arrays)
 * 
 * Note: Does not work with functions, Date objects, or circular references.
 * For battle engine purposes, this is sufficient for state objects.
 */
export function deepClone<T>(obj: T): T {
  if (obj === null || typeof obj !== "object") {
    return obj;
  }

  if (obj instanceof Date) {
    return new Date(obj.getTime()) as unknown as T;
  }

  if (Array.isArray(obj)) {
    return obj.map(item => deepClone(item)) as unknown as T;
  }

  const cloned = {} as T;
  for (const key in obj) {
    if (Object.prototype.hasOwnProperty.call(obj, key)) {
      cloned[key] = deepClone(obj[key]);
    }
  }

  return cloned;
}

